import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
sns.set_style('darkgrid')

data = pd.read_csv('Customer Prediction.txt')

data

data.describe(include='all')

data.isnull().sum()

data['Churn'] = data['Churn'].map({'Yes':1,'No':0})
data['PaymentMethod'] = data['PaymentMethod'].map({'Electronic check':0,'Mailed check':1,'Bank transfer':2,'Credit card':3})
data['Contract'] = data['Contract'].map({'Month-to-month':0,'One year':1,'Two year':2})
data['InternetService']=data['InternetService'].map({'DSL':0,'Fiber optic':1})
data['Gender'] = data['Gender'].map({'Male':0,'Female':1})

data

data_with_no_mv = data.dropna(axis=0)

data_with_no_mv

data_with_no_mv.isnull().sum()

data_r = data_with_no_mv.drop('CustomerID',axis=1)

data_r

X = data_r.drop('Churn',axis=1)

X

y = data_r.Churn

y

from sklearn.model_selection import train_test_split

X_train,X_test,y_train,y_test = train_test_split(X,y,test_size=0.2,random_state=0)

X_train.shape

X_test.shape

from sklearn.naive_bayes import GaussianNB

model = GaussianNB()

model.fit(X_train,y_train)

y_test

y_pred = model.predict(X_test)

y_pred

from sklearn.metrics import confusion_matrix,classification_report

print(confusion_matrix(y_test,y_pred))

print(classification_report(y_test,y_pred))

def predict_churn_simple( Gender, SeniorCitizen, Tenure,MonthlyCharges,
                        Contract, PaymentMethod, InternetService):
    result = model.predict([[ Gender, SeniorCitizen, Tenure, MonthlyCharges, Contract, PaymentMethod, InternetService]])
    return "Customer will leave ❌" if result[0] == 1 else "Customer will stay ✅"

predict_churn_simple(0,0,1,29.85,0,0,0.0)

predict_churn_simple(0,0,50,45.25,2,2,0.0)

